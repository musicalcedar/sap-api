export const getDepartmentCode = (departmentName: string): string => {
  const normalizedName = departmentName.toUpperCase().trim();
  return departmentDictionary[normalizedName] || '05';
};

export const getMunicipalityCode = (municipalityName: string, departmentCode: string): string => {
  const normalizedName = municipalityName.toUpperCase().trim();
  const municipalities = municipalityDictionary[departmentCode];

  if (!municipalities) {
    return departmentCode + '001';
  }

  return municipalities[normalizedName] || departmentCode + '001';
};

export const getMunicipalityCodeByNames = (
  municipalityName: string,
  departmentName: string
): string => {
  const departmentCode = getDepartmentCode(departmentName);
  return getMunicipalityCode(municipalityName, departmentCode);
};

